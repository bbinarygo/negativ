/* wizard.js — 3-step contributor wizard
   Step 1: Identity (code, category, severity, httpStatus)
   Step 2: Content + live preview (title, description, recoveryAction)
   Step 3: Review JSON + validate + open GitHub Issue                  */

const RAW = 'https://raw.githubusercontent.com/bbinarygo/negativ/main/';
const ISSUE_URL = 'https://github.com/bbinarygo/negativ/issues/new';

let step = 1;

// ── Init ─────────────────────────────────────────────────────────────────
async function wizardInit() {
  await loadCategories();
  gotoStep(1);
  bindInputs();
}

async function loadCategories() {
  try {
    const res = await fetch(RAW + 'registry/index.json');
    const index = await res.json();
    const cats = [...new Set(index.codes.map(c => c.category))].sort();
    const sel = document.getElementById('w-category');
    cats.forEach(c => { const o = document.createElement('option'); o.value = c; o.textContent = c; sel.appendChild(o); });
  } catch { /* fallback: user types manually */ }
}

// ── Navigation ───────────────────────────────────────────────────────────
function gotoStep(n) {
  step = n;
  document.querySelectorAll('.wiz-step').forEach((el, i) => {
    el.hidden = (i + 1) !== n;
  });
  document.querySelectorAll('.progress-pip').forEach((el, i) => {
    el.classList.toggle('active', i < n);
  });
  if (n === 3) buildReview();
}

window.wizardNext = () => {
  if (step === 1 && !validateStep1()) return;
  if (step === 2 && !validateStep2()) return;
  gotoStep(Math.min(step + 1, 3));
};
window.wizardBack = () => gotoStep(Math.max(step - 1, 1));

// ── Validation ───────────────────────────────────────────────────────────
function validateStep1() {
  const code = val('w-code');
  const ok = /^NEG-\d{3}-[a-z]+-[a-z][a-z0-9-]+$/.test(code);
  setErr('err-code', ok ? '' : 'Format must be NEG-<3digits>-<category>-<subcode> (e.g. NEG-422-validation-duplicate)');
  return ok;
}
function validateStep2() {
  const title = val('w-title').trim();
  const recovery = val('w-recovery').trim();
  const ok = title.length > 0 && recovery.length > 0;
  setErr('err-content', ok ? '' : 'Title and Recovery action are required');
  return ok;
}

// ── Live preview ─────────────────────────────────────────────────────────
function bindInputs() {
  ['w-title', 'w-description', 'w-recovery'].forEach(id => {
    document.getElementById(id)?.addEventListener('input', updatePreview);
  });
}
function updatePreview() {
  document.getElementById('prev-title').textContent = val('w-title') || 'Error title';
  document.getElementById('prev-msg').textContent   = val('w-description') || 'Error description appears here.';
  document.getElementById('prev-cta').textContent   = val('w-recovery') || 'Recovery action';
}

// ── Build JSON + review ──────────────────────────────────────────────────
function buildJSON() {
  const code = val('w-code').trim();
  const parts = code.toLowerCase().split('-'); // NEG-401-auth-session-expired
  const category = val('w-category') || (parts[2] || '');
  return {
    code,
    category,
    httpStatus: parseInt(val('w-status'), 10) || null,
    severity: val('w-severity') || 'error',
    title: val('w-title').trim(),
    description: val('w-description').trim(),
    messageKey: category + '.' + (parts.slice(3).join('-') || 'error'),
    recoveryAction: val('w-recovery').trim(),
    platforms: ['web', 'mobile', 'tablet'],
    accessibility: { ariaRole: 'alert', ariaLive: 'polite' }
  };
}

function buildReview() {
  const json = buildJSON();
  document.getElementById('review-json').textContent = JSON.stringify(json, null, 2);
  validateJSON(json);
}

function validateJSON(json) {
  const errs = [];
  ['code', 'category', 'title', 'messageKey', 'recoveryAction'].forEach(k => {
    if (!json[k]) errs.push('Missing required field: ' + k);
  });
  if (!['info', 'warning', 'error'].includes(json.severity)) errs.push('severity must be one of: info, warning, error');
  const errEl = document.getElementById('review-errors');
  const submitEl = document.getElementById('btn-submit');
  if (errs.length) {
    errEl.innerHTML = errs.map(e => '<li>' + e + '</li>').join('');
    errEl.hidden = false;
    submitEl.disabled = true;
  } else {
    errEl.hidden = true;
    submitEl.disabled = false;
  }
}

// ── Submit → GitHub Issue URL ────────────────────────────────────────────
window.wizardSubmit = () => {
  const json = buildJSON();
  const title = encodeURIComponent('feat(registry): add ' + json.code);
  const body = encodeURIComponent(
    '## New error code: ' + json.code + '\n\n' +
    '**Category:** ' + json.category + '  \n' +
    '**Severity:** ' + json.severity + '  \n\n' +
    '### index.json\n```json\n' + JSON.stringify(json, null, 2) + '\n```\n\n' +
    '---\n*Generated by the negativ contributor wizard*'
  );
  window.open(ISSUE_URL + '?template=error-code.yml&title=' + title + '&body=' + body, '_blank');
};

// ── Helpers ──────────────────────────────────────────────────────────────
function val(id) { return (document.getElementById(id)?.value || ''); }
function setErr(id, msg) { const el = document.getElementById(id); if (el) { el.textContent = msg; el.hidden = !msg; } }

document.addEventListener('DOMContentLoaded', () => {
  wizardInit();
  const det = document.getElementById('wiz-details');
  if (det) {
    det.open = localStorage.getItem('negativ-wiz-open') !== 'false';
    det.addEventListener('toggle', () => {
      localStorage.setItem('negativ-wiz-open', String(det.open));
    });
  }
});
